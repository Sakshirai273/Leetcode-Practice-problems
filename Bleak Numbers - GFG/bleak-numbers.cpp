class Solution
{
public:
	int is_bleak(int n)
	{
	    // Code here.
	    int life=31;
	    int x=n;
	    while(life-- and x>=0){
	        if(x+__builtin_popcount(x)==n)return 0;
	        x--;
	    }
	    return 1;
	}
};


//{ Driver Code Starts.
int main(){
    int T;
    cin >> T;
    while(T--)
    {
    	int n; 
    	cin >> n;
    	Solution ob;
    	int ans = ob.is_bleak(n);
    	cout << ans << "\n";
    }
	return 0;
}

